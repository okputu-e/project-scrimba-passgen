const passwordBtn = document.getElementById("password-btn");
const passwordOneEl = document.getElementById("pass-one-el");
const passwordTwoEl = document.getElementById("pass-two-el");

passwordBtn.addEventListener("click", () => {
  passwordOneEl.textContent = generatePassword();
  passwordTwoEl.textContent = generatePassword();
});

function generatePassword() {
  let password = "";
  for (let i = 0; i < characters.length; i++) {
    let randomNum = Math.floor(Math.random() * characters.length) + 1;
    password += characters[randomNum];
  }
  return password.substring(0, 15);
}
